function getVisibleCafes() {
  return cafes
    .filter((cafe) => {
      const cafeMatch = !state.selectedCafeFilters.size || state.selectedCafeFilters.has(cafe.id);
      const distanceMatch = matchesDistanceFilter(cafe);
      return cafeMatch && distanceMatch;
    })
    .sort((a, b) => {
      if (state.userLocation) {
        return getDistanceFromUser(a) - getDistanceFromUser(b);
      }
      return a.name.localeCompare(b.name);
    });
}

