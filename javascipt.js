function renderMenuCarousel() {
  if (!state.activeMenuCafe) {
    return;
  }

  const cafeName = state.activeMenuCafe.name;
  if (!state.activeMenuImages.length) {
    const socialLinks = state.activeMenuCafe.socialLinks ?? {};
    elements.menuGallery.innerHTML = `
      <div class="menu-empty">
        <div class="menu-empty__icon" aria-hidden="true">i</div>
        <h4>Menu not available</h4>
        <p>${state.activeMenuCafe.menuNotice ?? "The menu is not available here yet. Please check the cafe's social media pages for updates."}</p>
        <div class="menu-empty__actions">
          ${socialLinks.instagram ? `<a href="${socialLinks.instagram}" target="_blank" rel="noreferrer">Instagram</a>` : ""}
          ${socialLinks.facebook ? `<a href="${socialLinks.facebook}" target="_blank" rel="noreferrer">Facebook</a>` : ""}
        </div>
      </div>
    `;
    elements.menuDots.innerHTML = "";
    elements.menuPrev.disabled = true;
    elements.menuNext.disabled = true;
    return;
  }

  const imagePath = state.activeMenuImages[state.activeMenuIndex];
  const isPdf = imagePath.toLowerCase().endsWith(".pdf");
  elements.menuGallery.innerHTML = isPdf
    ? `<iframe class="menu-carousel__pdf" src="${imagePath}" title="${cafeName} menu PDF"></iframe>`
    : `<img src="${imagePath}" alt="${cafeName} menu page ${state.activeMenuIndex + 1}">`;
  fitMenuMediaForWebView();
  elements.menuDots.innerHTML = state.activeMenuImages
    .map((_, index) => `<span class="menu-carousel__dot ${index === state.activeMenuIndex ? "is-active" : ""}"></span>`)
    .join("");
  elements.menuPrev.disabled = state.activeMenuIndex === 0;
  elements.menuNext.disabled = state.activeMenuIndex === state.activeMenuImages.length - 1;
}

function changeMenuSlide(direction) {
  const nextIndex = state.activeMenuIndex + direction;
  if (nextIndex < 0 || nextIndex >= state.activeMenuImages.length) {
    return;
  }

  state.activeMenuIndex = nextIndex;
  renderMenuCarousel();
}

function fitMenuMediaForWebView() {
  const media = elements.menuGallery.querySelector("img, iframe");
  if (!media) {
    return;
  }

  media.setAttribute("draggable", "false");

  if (media.tagName.toLowerCase() === "img" && "decode" in media) {
    media.decode().catch(() => {});
  }
}

