function getOpenStatus(cafe) {
  const now = new Date();
  const todayHours = cafe.hours[now.getDay()];

  if (!todayHours) {
    return { isOpen: false, label: t("closedToday") };
  }

  const currentMinutes = now.getHours() * 60 + now.getMinutes();
  const [openAt, closeAt] = todayHours.map(toMinutes);
  const isOpen = currentMinutes >= openAt && currentMinutes <= closeAt;

  return {
    isOpen,
    label: isOpen ? `${t("openUntil")} ${formatTime12(todayHours[1])}` : `${t("opensAt")} ${formatTime12(todayHours[0])}`
  };
}

function buildHoursLabel(cafe) {
  const dayNames = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];
  const today = new Date().getDay();
  const hours = cafe.hours[today];
  return hours ? `${dayNames[today]} ${t("hours")}: ${formatTime12(hours[0])} - ${formatTime12(hours[1])}` : `${dayNames[today]} ${t("hoursUnavailable")}`;
}

function getDistanceFromUser(cafe) {
  const fallback = getDistanceKm(
    { lat: 11.9674, lng: 121.9242 },
    { lat: cafe.coords[0], lng: cafe.coords[1] }
  );

  if (!state.userLocation) {
    return fallback;
  }

  return getDistanceKm(
    { lat: state.userLocation.lat, lng: state.userLocation.lng },
    { lat: cafe.coords[0], lng: cafe.coords[1] }
  );
}

function matchesDistanceFilter(cafe) {
  const selectedDistance = elements.distanceDropdown.dataset.value || "all";
  if (selectedDistance === "all") {
    return true;
  }

  return getDistanceFromUser(cafe) <= Number(selectedDistance);
}

function getDistanceKm(a, b) {
  const earthRadius = 6371;
  const dLat = toRadians(b.lat - a.lat);
  const dLng = toRadians(b.lng - a.lng);
  const lat1 = toRadians(a.lat);
  const lat2 = toRadians(b.lat);

  const haversine =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1) * Math.cos(lat2) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);

  const c = 2 * Math.atan2(Math.sqrt(haversine), Math.sqrt(1 - haversine));
  return earthRadius * c;
}

function formatDistance(distanceKm) {
  if (distanceKm < 1) {
    return `${Math.round(distanceKm * 1000)} m`;
  }
  return `${distanceKm.toFixed(1)} km`;
}

function estimateTravelTime(distanceKm) {
  const walkingMinutes = Math.max(3, Math.round((distanceKm / 4.8) * 60));
  const eTrikeMinutes = Math.max(2, Math.round((distanceKm / 18) * 60));
  return `${walkingMinutes} ${t("minWalk")} | ${eTrikeMinutes} ${t("minRide")}`;
}

async function showRouteToCafe(cafeId) {
  const cafe = cafes.find((item) => item.id === cafeId);
  if (!cafe) {
    return;
  }

  state.selectedCafeId = cafeId;
  const origin = getRouteOrigin();
  const destination = { lat: cafe.coords[0], lng: cafe.coords[1] };

  elements.routeCafeName.textContent = cafe.name;
  elements.routeMeta.innerHTML = buildRouteMeta(getDistanceKm(origin, destination));
  elements.routeCard.classList.remove("is-hidden");
  elements.statusText.textContent = `${t("statusPreviewing")} ${cafe.name}.`;

  removeRouteLine();
  setRouteLoading(true, cafe.name);
  const routePoints = await fetchRoute(origin, destination);
  setRouteLoading(false);
  if (state.selectedCafeId !== cafeId) {
    return;
  }

  const pointsToDraw = routePoints.length ? routePoints : [
    [origin.lat, origin.lng],
    [destination.lat, destination.lng]
  ];

  state.routeLine = L.polyline(pointsToDraw, {
    color: "#f4b321",
    weight: 8,
    opacity: 0.95
  }).addTo(map);

  map.fitBounds(state.routeLine.getBounds(), {
    padding: [40, 40]
  });

  const routeDistanceKm = routePoints.length ? getRouteDistanceKm(routePoints) : getDistanceKm(origin, destination);
  elements.routeMeta.innerHTML = buildRouteMeta(routeDistanceKm);

  if (state.navigationActive) {
    updateNavigationBanner(cafe.name, routeDistanceKm);
  }
}

