function locateUser() {
  if (!navigator.geolocation) {
    elements.statusText.textContent = "Geolocation is not supported on this device.";
    return;
  }

  elements.statusText.textContent = "Getting your GPS location...";

  navigator.geolocation.getCurrentPosition(
    (position) => {
      state.userLocation = {
        lat: position.coords.latitude,
        lng: position.coords.longitude
      };

      userMarker.setLatLng([state.userLocation.lat, state.userLocation.lng]).addTo(map);
      map.setView([state.userLocation.lat, state.userLocation.lng], 15);
      elements.statusText.textContent = t("statusLiveLocation");
      render();
      if (state.selectedCafeId) {
        showRouteToCafe(state.selectedCafeId);
      }
    },
    () => {
      elements.statusText.textContent = "Location access was blocked. Showing default Boracay view.";
    },
    {
      enableHighAccuracy: true,
      timeout: 10000
    }
  );
}

