function refreshTikTokEmbeds() {
  const previousScript = document.getElementById("tiktokEmbedScript");
  if (previousScript) {
    previousScript.remove();
  }

  const script = document.createElement("script");
  script.id = "tiktokEmbedScript";
  script.async = true;
  script.src = "https://www.tiktok.com/embed.js";
  document.body.append(script);
}

function waitForTikTokEmbed() {
  const loadingElement = document.getElementById("tiktokLoading");
  if (!loadingElement) {
    return;
  }

  let attempts = 0;
  const maxAttempts = 40;

  const checkEmbed = () => {
    const iframe = elements.tiktokViewer.querySelector("iframe");
    const blockquote = elements.tiktokViewer.querySelector(".tiktok-embed");
    const hasRenderedEmbed = iframe || (blockquote && blockquote.classList.contains("tiktok-embed-rendered"));

    attempts += 1;

    if (hasRenderedEmbed || attempts >= maxAttempts) {
      loadingElement.classList.add("is-hidden");
      return;
    }

    window.setTimeout(checkEmbed, 250);
  };

  window.setTimeout(checkEmbed, 250);
}

